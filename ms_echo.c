#include "my_shell.h"

void		ms_echo(int ac, char **av)
{
	int	i;

	i = 1;
	while (i < ac)
	{
		ft_putstr(av[i++]);
		if (av[i])
			write(1, " ", 1);
	}
	write(1, "\n", 1);
}
